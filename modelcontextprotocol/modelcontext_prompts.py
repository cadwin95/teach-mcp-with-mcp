# 이 파일은 MCP에서 프롬프트(메시지) 구조와 대화 흐름의 개념을 설명합니다.
# 프롬프트는 LLM이 사용자와 대화하거나, 툴을 호출할 때 사용하는 메시지의 표준 구조입니다.
# MCP에서는 messages 리스트와 tool call 결과를 조합해 대화 흐름을 관리합니다.

messages = [
    {"role": "user", "content": "2와 3을 더해줘."},
]

# 예시: 툴 호출 결과를 대화에 반영
messages.append({
    "role": "tool",
    "name": "add",
    "content": "5"
})

# 이후 LLM은 이 메시지 히스토리를 바탕으로 추가 답변을 생성할 수 있습니다.
# MCP 클라이언트/서버는 이런 메시지 구조를 표준화해 다양한 LLM/툴 연동을 쉽게 만듭니다. 